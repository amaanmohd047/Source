# to print from 1 to 20 we can use print statement for 20 times or use loops

# range is used to define the range(kinda like limit) of the for loop
print("this will print from 0 to 19")

for i in range(20):
    print(i, end=" ")

print("\n")

print("this will print from 1 to 20")

for j in range(20):
    print(j + 1, end=" ")

print("\n")
